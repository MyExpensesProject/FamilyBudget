using Expenses.Domain.Mapping;

namespace FamilyBudget.Extensions;

/// <summary>
/// Mapping extensions
/// </summary>
public static class MappingExtensions
{
    /// <summary>
    /// AutoMapper extensions
    /// </summary>
    /// <param name="builder"> Builder </param>
    public static void AutoMapperExtensions(WebApplicationBuilder? builder)
    {
        builder?.Services.AddAutoMapper(typeof(ExpensesMappingProfile));
    }
}